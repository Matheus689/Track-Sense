SELECT 'BH' AS filial, m.numMaquina, s.idSensor,
    COUNT(r.idRegistro) AS registros_hoje,
    ROUND((COUNT(r.idRegistro) / 80) * 100, 0) AS eficiencia_pct
FROM maquina m JOIN sensor s ON s.fkMaquina = m.idMaquina
LEFT JOIN registroSensor r ON r.fkSensor = s.idSensor AND DATE(r.hrRegistro) = CURDATE()
WHERE m.fkEmpresaMaquina = 1002
GROUP BY m.idMaquina, m.numMaquina, s.idSensor ORDER BY eficiencia_pct DESC;

